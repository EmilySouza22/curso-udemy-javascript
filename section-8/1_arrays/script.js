/* Seção 8: Arrays e Objetos */

/*
    Arrays

    - Possibilidade de adicionar um conjunto de valores a uma variável
    - O array deve ser escrito entre colchetes, separando os valores por vírgulas
*/

/* Exemplo 1 */
let numeros = [1, 3, 5, 8, 12];
let informacoes = ['Matheus', 12, true, 'Teste', 2];

/* Exemplo 2 */
let arr = [1, 4, 5, 6, 7];

/* Exemplo 3 */
let nomes = ['Matheus', 'João', 'Alexia'];

/* Exemplo 4 */
let bool = [true, false, true];

/* Exemplo 5 */
let misturando = [1, 'Matheus', true];

console.log(arr);
console.log(nomes);
console.log(bool);
console.log(misturando);

/* 
    Mais sobre arrays

    Podemos acessar um array pelo seu índice, por exemplo: arr[1]
    Lembrando que o primeiro índice sempre é 0, ou seja, para acessar o primeiro elemento: arr[0]
*/

console.log(arr[1])
console.log(nomes[0])
console.log(bool[2])
console.log(arr[arr.length - 1])


