/* 
    Métodos 

    Métodos são propriedades que funcionam como funções
    Strings e arrays contém métodos
*/

/* Exemplo 1 */
let marca = 'Nike';
console.log(typeof marca.toUpperCase);
console.log(marca.toUpperCase());

/* Exemplo 2 */
let marca2 = marca.toLocaleUpperCase();
console.log(marca2.toLowerCase());

//Métodos servem para transformar os dados
//É importante saber a diferença entre propriedades e métodos

